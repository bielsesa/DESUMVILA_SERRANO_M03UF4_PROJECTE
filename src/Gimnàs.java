public class Gimnàs {
    private String nomGimnas;
    private String adreça;
    private String NIF;
    private int telefon;

    // GETTERS & SETTERS

    public String getNomGimnas() {
        return nomGimnas;
    }

    public void setNomGimnas(String nomGimnas) {
        this.nomGimnas = nomGimnas;
    }

    public String getAdreça() {
        return adreça;
    }

    public void setAdreça(String adreça) {
        this.adreça = adreça;
    }

    public String getNIF() {
        return NIF;
    }

    public void setNIF(String NIF) {
        this.NIF = NIF;
    }

    public int getTelefon() {
        return telefon;
    }

    public void setTelefon(int telefon) {
        this.telefon = telefon;
    }
}
